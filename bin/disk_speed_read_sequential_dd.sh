set -euo pipefail
[ -n "${DEBUG:-}" ] && set -x
srcdir="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# shellcheck disable=SC1090,SC1091
. "$srcdir/lib/utils.sh"

# shellcheck disable=SC2034,SC2154
usage_description="
Runs a sequential read test from the given file in order to test the sequential read speed

Uses dd and bypasses filesystem cache for a more accurate test

Useful for testing:

- different disks' speeds
- different cables' speed with the same disk
- different ports' speeds on Macs (right ports may be slower)

I wrote this because I discovered a huge performance and estimated time to restore speed difference using
macOS Time Machine recovery while using USB 2 vs USB 3 cables with the same SanDisk Extreme Pro SSD external backup disk

Generate a file to read using the adjacent script:

    NODELETE=1 disk_write_speed_sequential_dd.sh

If a directory is given will look for the following file generated by the above script:

    disk_speed_test_sequential_dd.bin
"

# used by usage() in lib/utils.sh
# shellcheck disable=SC2034
usage_args="[<file_or_directory>]"

help_usage "$@"

max_args 1 "$@"

file="${1:-${PWD:-$(pwd)}}"
if [ -d "$file" ]; then
    file="$file/disk_speed_test_sequential_dd.bin"
fi
file="${file//\/\//\/}"

timestamp "Sequential read test from: $file"
echo >&2

dd if="$file" of=/dev/null bs=64m iflag=direct

echo >&2
timestamp "Sequential read test completed"
